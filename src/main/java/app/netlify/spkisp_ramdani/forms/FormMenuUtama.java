        SLAnimator.start();
        sLPanel2.setTweenManager(SLAnimator.createTweenManager());
        sLPanel2.initialize(mainCfg);
        jPanel1.setCursor(new Cursor(Cursor.HAND_CURSOR) {
        });
        fnGantiMenu(new PanelBeranda());
